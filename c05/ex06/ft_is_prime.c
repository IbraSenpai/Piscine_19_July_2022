int	ft_is_prime(int nb)
{
	int	div;

	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	div = nb - 1;
	while (div > 1)
	{
		if (nb % div == 0)
			return (0);
		div--;
	}
	return (1);
}

